import UIKit

extension UIColor {
    
    final class var lightBlue: UIColor {
        get {
            return UIColor(red: 0.0, green: 0.5, blue: 1.0, alpha: 1.0)
        }
    }
    
    final class var darkGreen: UIColor {
        get {
            return UIColor(red: 0.0, green: 0.5, blue: 0.0, alpha: 1.0)
        }
    }
}
